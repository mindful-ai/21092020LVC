# Print the number of times each vowel occurs in the
# user input text

# input

text = input("Enter some text: ")

# process
# output

print("A -------> ", text.lower().count('a'))
print("E -------> ", text.lower().count('e'))
print("I -------> ", text.lower().count('i'))
print("O -------> ", text.lower().count('o'))
print("U -------> ", text.lower().count('u'))
